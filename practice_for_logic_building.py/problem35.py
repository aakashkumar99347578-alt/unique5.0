# problem statment :-   Print the squares of numbers from 1 to n. and Print cubes of numbers from 1 to n


#solution

num=int(input("enter the number for you want to print square where"))

for i in range(1,num+1):
    print(i*i)



num=int(input("enter the number for you want to print cube where"))

for i in range(1,num+1):
    print(i*i*i)
