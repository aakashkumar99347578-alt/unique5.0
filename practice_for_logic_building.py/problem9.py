# problem statment :- Take a character and check if it’s a vowel or consonant.


#solution


def check_character_vowel_constant(character):


    if character=="a" or "e" or "i" or "o" or "u":
        print(character,"is a vowel")


    else:
        print(character,"is a consonant")

check_character_vowel_constant("a")
check_character_vowel_constant("z")



